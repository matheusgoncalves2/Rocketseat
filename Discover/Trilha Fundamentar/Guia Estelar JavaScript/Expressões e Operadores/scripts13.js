/*
    Typecasting or Type coersion
*/

/*
    FALSY
    when a value is considered false in contexts where a boolean is required ( conditionals and loops)

    false
    0
    -0
    ""
    null
    undefined
    NaN
*/

console.log( true ? 'verdadeiro' : 'falso' )

/*
    TRUTHY
    when a value is considered true in contexts where a boolean is required ( conditionals and loops)

    true
    {}
    []
    1
    3.23
    "0"
    "false"
    -1
    Infinity
    -Infinity
*/

console.log( {} ? 'verdadeiro' : 'falso' )